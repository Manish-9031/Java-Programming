public class foreach {
    public static void main(String[] args) {
        int a[]={1,4,6,67,7};
        System.out.println(a[2]);
        System.out.println(a[3]);

        // For each loop is a loop to featch arry element one by one 

        for(int b : a){
            System.out.println(b +" " );
        }
    }
}
